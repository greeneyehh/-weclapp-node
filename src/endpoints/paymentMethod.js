const {buildUrl} = require('../utils')

module.exports = {

	/**
	 * @param page
	 * @param pageSize
	 * @param sort
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getPaymentMethods(fetch, {page, pageSize, sort, ...rest}) {
		return fetch(buildUrl('paymentMethod', {page, pageSize, sort, ...rest}))
	},

	/**
	 * @param body
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async createPaymentMethod(fetch, body) {
		return fetch('paymentMethod', {method: 'POST', body})
	},

	/**
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getPaymentMethodCount(fetch) {
		return fetch('paymentMethod/count')
	},

	/**
	 * @param id
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async deletePaymentMethodById(fetch, id) {
		return fetch(buildUrl(`paymentMethod/id/${id}`, {id}), {method: 'DELETE'})
	},

	/**
	 * @param id
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getPaymentMethodById(fetch, id) {
		return fetch(buildUrl(`paymentMethod/id/${id}`, {id}))
	},

	/**
	 * @param id
	 * @param body
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async updatePaymentMethodForId(fetch, id, body) {
		return fetch(buildUrl(`paymentMethod/id/${id}`, {id}), {method: 'PUT', body})
	}
}
