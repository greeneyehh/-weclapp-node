const {buildUrl} = require('../utils')

module.exports = {

	/**
	 * @param page
	 * @param pageSize
	 * @param sort
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getCustomerCategories(fetch, {page, pageSize, sort, ...rest}) {
		return fetch(buildUrl('customerCategory', {page, pageSize, sort, ...rest}))
	},

	/**
	 * @param body
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async createCustomerCategory(fetch, body) {
		return fetch('customerCategory', {method: 'POST', body})
	},

	/**
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getCustomerCategoryCount(fetch) {
		return fetch('customerCategory/count')
	},

	/**
	 * @param id
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async deleteCustomerCategoryById(fetch, id) {
		return fetch(buildUrl(`customerCategory/id/${id}`, {id}), {method: 'DELETE'})
	},

	/**
	 * @param id
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async getCustomerCategoryById(fetch, id) {
		return fetch(buildUrl(`customerCategory/id/${id}`, {id}))
	},

	/**
	 * @param id
	 * @param body
	 * @returns {Promise<*|Promise<*>|Promise|Promise<Response>|never>}
	 */
	async updateCustomerCategoryForId(fetch, id, body) {
		return fetch(buildUrl(`customerCategory/id/${id}`, {id}), {method: 'PUT', body})
	}
}
